
package corejava.practice;


public class AssignmentDemo {
    
    
    public static void main (String[] args){
        
        int x=3;
        int y=2;
        
        x+=y; //x=x+y 5;
        System.out.println("X ="+x);
        
         x-=y; //x=x-y; 4;
         
         
         
        System.out.println("X ="+x);
         x%=y; //x=x%y 
        System.out.println("X ="+x);
         x/=y; //x=x/y;
        System.out.println("X ="+x);
         x*=y; //x=x*y;
        System.out.println("X ="+x);
        
        
    }
    
}
