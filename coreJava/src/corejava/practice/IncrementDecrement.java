
package corejava.practice;


public class IncrementDecrement {
    
    public static void main(String[] args){
        int x=15;
        int y;
        int d=30;
        int c;
        y=x++; // x=15
        System.out.println(y);
        y=x; //x=16
        System.out.println(y);           
        c=d--; //d=30
        System.out.println(c);
        c=d; //d=29
        System.out.println(c);
        y=--x; // x=15
        System.out.println(y);
        y=++x; // x=16
        System.out.println(y);           
        c=d--; //d =29
        System.out.println(c);
        c=d; //d=28
        System.out.println(c);
        
   
}
    
    /* That's end for example increment and decrement operator  */
    
    
}
