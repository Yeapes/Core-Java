
package hashSet;

import java.util.HashSet;


public class HashSetDemo {
    public static void main(String[] args){
        
        //Note: Duplicate value are not allowed in HashSet
        HashSet<String> mobileName=new HashSet<String>();
        
        mobileName.add("Samsung");
        mobileName.add("Blackerry");
        mobileName.add("i-Phone6");
        mobileName.add("Oppo");
        
        System.out.println(mobileName);
        
        
        
        
    }
    
    
    
    
    
    
}
